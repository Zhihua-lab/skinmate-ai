function buildDemoResponse(sourceUrl, detail) {
  const analysis = buildDemoAnalysis(sourceUrl);
  return {
    success: true,
    video_id: analysis.video_id,
    markdown: [
      '# Demo Analysis',
      '',
      `- Source URL: ${sourceUrl}`,
      `- Fallback: ${detail}`,
    ].join('\n'),
    analysis,
  };
}

async function proxyToBackend(backend, body) {
  const response = await fetch(`${backend}/analyze-video`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body,
  });
  const raw = await response.text();
  if (!response.ok) {
    throw new Error(raw || `Backend responded with HTTP ${response.status}`);
  }

  try {
    const parsed = JSON.parse(raw);
    if (parsed && parsed.analysis) {
      return {
        statusCode: 200,
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(parsed),
      };
    }
  } catch {
    // Fall through to demo fallback.
  }

  throw new Error('Backend response did not contain a valid analysis payload');
}

exports.handler = async (event) => {
  if (event.httpMethod !== 'POST') {
    return {
      statusCode: 405,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ detail: 'Method not allowed' }),
    };
  }

  let payload = {};
  try {
    payload = JSON.parse(event.body || '{}');
  } catch {
    payload = {};
  }

  const sourceUrl = String(payload.url || '').trim();
  if (!sourceUrl) {
    return {
      statusCode: 400,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ detail: 'url is required' }),
    };
  }

  const backend = (process.env.BACKEND_URL || process.env.VITE_API_BASE || '').replace(/\/$/, '');
  if (!backend) {
    return {
      statusCode: 200,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(buildDemoResponse(sourceUrl, 'BACKEND_URL is not configured')),
    };
  }

  try {
    return await proxyToBackend(backend, event.body);
  } catch (error) {
    return {
      statusCode: 200,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(buildDemoResponse(sourceUrl, `backend unavailable: ${error.message}`)),
    };
  }
};
