function extractVideoId(sourceUrl = '') {
  const text = String(sourceUrl || '');
  const modalMatch = text.match(/[?&]modal_id=(\d{12,})/i);
  if (modalMatch) return modalMatch[1];
  const pathMatch = text.match(/\/video\/(\d{12,})/i);
  if (pathMatch) return pathMatch[1];
  const numericMatch = text.match(/(\d{12,})/);
  return numericMatch ? numericMatch[1] : 'demo';
}

